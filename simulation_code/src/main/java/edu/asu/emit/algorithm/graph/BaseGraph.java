package edu.asu.emit.algorithm.graph;

import java.util.List;

/**
 * The interface defines the minimal requirement for a graph
 * to participate in the algorithms.
 * 
 * @author yqi
 * @author snkas
 */
public interface BaseGraph {
	List<Vertex> getVertexList();
	long getEdgeWeight(Vertex source, Vertex sink);
	List<Vertex> getAdjacentVertices(Vertex vertex);
	List<Vertex> getPrecedentVertices(Vertex vertex);
}
